#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		ft_print_program_name(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	return (str[i]);
}

int		main(int argc, char **argv)
{
	ft_print_program_name(argv[0]);
	ft_putchar('\n');
	return (0);
}
